class SiteSettings(BaseSettings):
    """
    –ö–ª–∞—Å—Å —Å–æ –≤—Å–µ–º –¥–∞–Ω–Ω—ã–º–∏ –¥–ª—è –≤—Ö–æ–¥–∞, —Ç—É—Ç —Ç–∞–∫ –∂–µ –ø—Ä–æ–≤–µ—Ä—è–µ—Ç—Å—è –Ω–∞ –Ω–∞–ª–∏—á–∏–µ —ç—Ç–∏—Ö –¥–∞–Ω–Ω—ã—Ö –≤ .env
    """
    BOT_TOKEN: SecretStr = os.getenv('BOT_TOKEN', None)
    if BOT_TOKEN is None:
        exit('BOT_TOKEN –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç –≤ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –æ–∫—Ä—É–∂–µ–Ω–∏—è')

    BOT_ID: SecretStr = os.getenv('BOT_ID', None)
    if BOT_ID is None:
        exit('BOT_TOKEN –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç –≤ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –æ–∫—Ä—É–∂–µ–Ω–∏—è')

    API_KEY: SecretStr = os.getenv('API_KEY', None)
    if API_KEY is None:
        exit('API_KEY –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç –≤ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –æ–∫—Ä—É–∂–µ–Ω–∏—è')

    API_URL: StrictStr = os.getenv('API_URL', None)
    if API_URL is None:
        exit('API_URL –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç –≤ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –æ–∫—Ä—É–∂–µ–Ω–∏—è')
